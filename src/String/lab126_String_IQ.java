package String;

public class lab126_String_IQ {
    public static void main(String[] args) {
        char c = 'A';
        System.out.println(c);  // A
        String s1= "ABCD";      // SCP
        System.out.println(s1.length());    // 4
        System.out.println(s1.toLowerCase());   // abcd   // SCP
        System.out.println(s1.toUpperCase());   // ABCD   // SCP
        System.out.println(s1.concat("E"));   //  ABCDE //SCP
        System.out.println();
    }
}
