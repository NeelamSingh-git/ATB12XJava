package FOR_Loop;

public class lab102_loop_decrement {
    public static void main(String[] args) {
        for (int i = -1; i <-10 ; i--) {    //nothing print because -1 not < -10
            System.out.println(i);
        }
        for (int j = -1; j >-10 ; j--) {
            System.out.println(j);
        }
    }
}
