package Increment_Decrement;

public class lab67_Pre_increment {
    public static void main(String[] args) {
        int a = 10;     // a= a+1
        int b = ++a;    // 11
        System.out.println(a);
        System.out.println(b);
        //exp and result value
        // line no  /   a   /  b
        //   5      /   10  /  na
        //  6       /   11  /   11
        //  7       /print 11   / 11
        //  8   /  11   / print 11
    }
}
